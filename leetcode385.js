/**
 * @param {string} s
 * @return {NestedInteger}
 */
var deserialize = function(s) {
    let a = JSON.parse(s);
    return dfs(a);
};

function dfs(a){
    if(typeof a === 'number'){
        let ni = new NestedInteger();
        ni.setInteger(a);
        return ni;
    }
    let ni = new NestedInteger();
    for(let i = 0; i < a.length; i++){
        ni.add(dfs(a[i]));
    }
    return ni;
};